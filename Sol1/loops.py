def main() :

    print("\n while loop")
    count=0
    while (count<10):
        print(count)
        count=count+1

    print("\n for loop")
    for i in range(10):
        print(i)

    print("\n for loop with defined range")
    for i in range(20,25):
        print(i)

if __name__ == "__main__":
    main()